package statement;

public class Switch_1 {

	public static void main(String[] args) {
	int year=1;
		switch(year)
		{
		case 1:
		System.out.println("welcome 1st year");
		break;
		
		case 2:
	System.out.println("welcome 2nd year");
	break;
	
		case 3:
		System.out.println("welcome 3rd year");
		break;
		default:
			System.out.println("please enter values between 1-4");
		}

}
}
