package loops;

public class forLoop1 {

	public static void main(String[] args) {
		System.out.println("For Loop using assending order");
		int a=30;
		for(int i=1;i<=a;i++)
		{
			System.out.print(i+ " ");
			
		}
		System.out.print("For Loop using dssending order");
		System.out.println(" ");
		for(int j=30;j>=1;j--)
		{
			System.out.print(j+ " ");
		}
	}

}
